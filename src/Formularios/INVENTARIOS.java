    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        ConexionBD Con = new ConexionBD();
        Con.ConectarBD();
        String mod, mar, talla, prec, stock;
        mod = txtModelo.getText();
        mar = txtMarca.getText();
        talla = txtTalla.getText();
        prec = txtPrecio.getText();
        stock = txtStock.getText();
        if (!mod.isEmpty()) {
            if (!mar.isEmpty()) {
                if (!talla.isEmpty()) {
                    if (!prec.isEmpty()) {
                        if (!stock.isEmpty()) {
                            try {
                                String SQL = "INSERT INTO inventario"
                                        + "(modelo,marca,talla,precio,stock)"
                                        + " VALUES "
                                        + "('" + mod + "','" + mar + "','" + talla + "','" + prec + "','" + stock + "');";
                                Con.sentencia.execute(SQL);
                                JOptionPane.showMessageDialog(null, "¬°Los datos fueron guardados con √©xito!");
                            } catch (SQLException ex) {
                                JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Por favor ingrese el stock");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Por favor ingrese el precio");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Por favor ingrese la talla");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Por favor ingrese la marca");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el modelo");
        }
        Con.DesconectarBD();


    }//GEN-LAST:event_btnGuardarActionPerformed

