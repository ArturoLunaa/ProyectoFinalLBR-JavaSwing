    DefaultTableModel modelo;

    private void Crear_Modelo() {
        try {
            modelo = (new DefaultTableModel(
                    null, new String[]{
                         "ID", "idCliente", "idProducto", "fecha",
                        "total", "Forma de Pago"}) {
                Class[] types = new Class[]{
                    java.lang.String.class, java.lang.String.class,
                    java.lang.String.class, java.lang.String.class,
                    java.lang.String.class, java.lang.String.class,
                    
                };
                boolean[] canEdit = new boolean[]{
                    false, false, false, false, false, false, false};

                public Class getColumnClass(int columnIndex) {
                    return types[columnIndex];
                }

                public boolean isCellEditable(int rowIndex, int colIndex) {
                    return canEdit[colIndex];
                }
            });
            Tabla_Ventas.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString() + "Error");
        }
    }

    private void Consultar() {
        try {
            int i = 0;
            Object Ob[] = null;
            ConexionBD Con = new ConexionBD();
            Con.ConectarBD();
            String SQL = "SELECT * FROM ventas";
            Con.resultado = Con.sentencia.executeQuery(SQL);
            while (Con.resultado.next()) {
                modelo.addRow(Ob);
                modelo.setValueAt(Con.resultado.getString("id"), i, 0);
                modelo.setValueAt(Con.resultado.getString("idCliente"), i, 1);
                modelo.setValueAt(Con.resultado.getString("idProducto"), i, 2);
                modelo.setValueAt(Con.resultado.getString("fecha"), i, 3);
                modelo.setValueAt(Con.resultado.getString("total"), i, 4);
                modelo.setValueAt(Con.resultado.getString("formaPago"), i, 5);
               
                i++;
            }
            Con.DesconectarBD();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);

        }
    }


