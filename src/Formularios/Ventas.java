    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        ConexionBD Con = new ConexionBD();
        Con.ConectarBD();
        String fecha, formaPago, idCliente, idProducto, total;
        
        idCliente = txtidCliente.getText();
        idProducto = txtidProducto.getText();
        fecha = txtFecha.getText();
        total = txtTotal.getText();
        formaPago = cbxFormaPago.getSelectedItem().toString();
        if (!idCliente.isEmpty()) {
                if (!idProducto.isEmpty()) {
                    if (!fecha.isEmpty()) {
                        if (!total.isEmpty()) {
                            if (!formaPago.isEmpty()) {
                                
                                        try {
                                            String SQL = "INSERT INTO ventas"
                                                    + "(idCliente,idProducto,fecha,total,formaPago)"
                                                    + " VALUES "
                                                    + "('" + idCliente + "','" + idProducto + "','" + fecha + "','" + total + "','" + formaPago +"');";
                                            Con.sentencia.execute(SQL);
                                            JOptionPane.showMessageDialog(null, "¡Los datos fueron guardados con éxito!");
                                        } catch (SQLException ex) {
                                            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                                        }
                               
                            } else {
                                JOptionPane.showMessageDialog(null, "Por favor ingrese la forma de pago");
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Por favor ingrese el total del pago");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Por favor ingrese la fecha");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Por favor ingrese el id del producto");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Por favor ingrese el id del cliente");
        }
        Con.DesconectarBD();
       
    }//GEN-LAST:event_btnGuardarActionPerformed

